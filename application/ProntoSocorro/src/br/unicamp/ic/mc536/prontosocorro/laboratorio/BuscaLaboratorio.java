    private void search() {
        if (chSemFiltro.isSelected()){
            atualizaTabela(Laboratorio.consultar("",(short)-1));
            lbErro.setText("");
        } else if(chRegistro.isSelected()) {
            try {
                String nRegistro = edRegistro.getText();
                atualizaTabela(Laboratorio.consultar(edRegistro.getText(),(short)1));
                lbErro.setText("");
            } catch (NumberFormatException e) {
                lbErro.setText("O código deve ser um valor numérico!");
            }
        } else if(chNome.isSelected()) {
            atualizaTabela(Laboratorio.consultar(edNome.getText(),(short)2));
            lbErro.setText("");
        } else if(chTipo.isSelected()) {
            atualizaTabela(Laboratorio.consultar(edTipo.getText(),(short)3));
            lbErro.setText("");
        } 
    }
    
    protected void atualizaTabela(ResultSet rs){
        DefaultTableModel modelotabela = (DefaultTableModel) tabela.getModel();
        modelotabela.setColumnCount(5);
        modelotabela.setRowCount(0);

        tabela.getColumnModel().getColumn(0).setHeaderValue("Registro");
        tabela.getColumnModel().getColumn(1).setHeaderValue("Nome");
        tabela.getColumnModel().getColumn(2).setHeaderValue("Telefone");
        tabela.getColumnModel().getColumn(3).setHeaderValue("Tipo");
        tabela.getColumnModel().getColumn(4).setHeaderValue("Responsável");
        
        int linha = 0;
        try{
            if(rs != null) {  
                while(rs.next()){
                      modelotabela.addRow(new String[modelotabela.getColumnCount()]);
                      modelotabela.setValueAt(rs.getString("nRegistro"), linha, 0);
                      modelotabela.setValueAt(rs.getString("nome"), linha, 1);
                      modelotabela.setValueAt(rs.getString("telefone"), linha, 2);
                      modelotabela.setValueAt(rs.getString("tipo"), linha, 3);
                      modelotabela.setValueAt(rs.getString("responsavel"), linha, 4);
                      linha++;
                  }
            }
        }catch(SQLException e){
            System.err.println("Erro: " + e);
        }
    }

