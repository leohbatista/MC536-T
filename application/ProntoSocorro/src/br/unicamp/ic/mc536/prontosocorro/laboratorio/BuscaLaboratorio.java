    private void btRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btRemoverMouseClicked
        int nRegistro;
        if(tabela.getSelectedRowCount() != 0){
            nRegistro = Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(),0).toString());
            if((JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?","Confirmação",JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE)) == JOptionPane.YES_OPTION){
                Laboratorio l = new Laboratorio(nRegistro,"","","","");
                l.excluir();
                if (Laboratorio.verificaExclusao(nRegistro)){
                    lbErro.setText("");
                    JOptionPane.showMessageDialog(this, "Excluído com sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
                    btBuscarMouseClicked(evt);
                } else {
                    lbErro.setText("Erro ao excluir!");
                } 
            }
        } else {
            lbErro.setText("Selecione uma linha para excluir!");
        }
    }//GEN-LAST:event_btRemoverMouseClicked

    private void btAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAlterarMouseClicked
         if(tabela.getSelectedRowCount() != 0){
            int nRegistro = Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(),0).toString());
            ResultSet rs = Laboratorio.consultar(""+nRegistro,(short)1);
            try {
                lbErro.setText("");
                rs.next();
                Laboratorio laboratorio = new Laboratorio(nRegistro, rs.getString("nome"), rs.getString("telefone"),rs.getString("tipo"),rs.getString("responsavel"));
                InsereLaboratorio alt = new InsereLaboratorio(laboratorio);
                alt.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(BuscaLaboratorio.class.getName()).log(Level.SEVERE, null, ex);
            }   
        } else {
            lbErro.setText("Selecione uma linha para alterar");
        }
    }//GEN-LAST:event_btAlterarMouseClicked

